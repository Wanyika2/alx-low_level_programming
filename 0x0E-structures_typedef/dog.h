#ifndef DOG_STRUCT_H
#define DOG_STRUCT_H

/**
 * struct dog - Define a dog structure
 * @name: The name of the dog
 * @age: The age of the dog
 * @owner: The owner of the dog
 *
 * Description: This structure defines a dog with a name, age, and owner.
 */
struct dog
{
	char *name;
	float age;
	char *owner;
}
;

#endif /* DOG_STRUCT_H */
